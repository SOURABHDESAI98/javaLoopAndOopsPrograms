package javaAfternoon.day4;

// tables from 1 to 5

public class Tables {

	public static void main(String[] args) {

		System.out.println("Tables from 1 to 5");

		for (int i = 1; i <= 5; i++) {

			for (int j = 1; j <= 10; j++) {

				System.out.print(i * j);

			}
			System.out.println();

		}

	}

}
