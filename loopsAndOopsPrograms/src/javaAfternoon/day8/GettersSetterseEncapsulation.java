package javaAfternoon.day8;

public class GettersSetterseEncapsulation {

	private int modelID;
	private String name;
	private int speed;

	public void setId(int modelId) {

		this.modelID = modelId;

	}

	public void setname(String name) {

		this.name = name;

	}

	public void setspeed(int speed) {

		this.speed = speed;

	}

	public int getId() {

		return modelID;

	}

	public String getname() {

		return name;

	}

	public int getspeed() {

		return speed;

	}

}
